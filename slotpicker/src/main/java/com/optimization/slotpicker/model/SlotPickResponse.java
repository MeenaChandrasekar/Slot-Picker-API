package com.optimization.slotpicker.model;

public class SlotPickResponse {
    private String containerId;
    private Integer targetX;
    private Integer targetY;
    private String error;

    public SlotPickResponse(String error) {
        this.error = error;
    }

    public SlotPickResponse(String containerId, int targetX, int targetY) {
        this.containerId = containerId;
        this.targetX = targetX;
        this.targetY = targetY;
    }

    // Getters and setters
    public String getContainerId() { return containerId; }
    public void setContainerId(String containerId) { this.containerId = containerId; }

    public Integer getTargetX() { return targetX; }
    public void setTargetX(Integer targetX) { this.targetX = targetX; }

    public Integer getTargetY() { return targetY; }
    public void setTargetY(Integer targetY) { this.targetY = targetY; }

    public String getError() { return error; }
    public void setError(String error) { this.error = error; }
}
